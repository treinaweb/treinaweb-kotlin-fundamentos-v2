package operadores

fun main() {
    val a = 5
    val b = 2

    // Soma
    println(a + b)

    // Subtração
    println(a - b)

    // Multiplicação
    println(a * b)

    // Divisão
    println(a / b.toDouble())

    // Modulo
    println(a % b)

    // Concatenação
    val c = "Cleyson " + "Lima"
    println(c)
}